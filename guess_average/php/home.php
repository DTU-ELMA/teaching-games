<h2>A simple bidding game</h2>

<p> The rules of the game are simple:
Each participant bids a number between 0 and 100.
You can change your bid by bidding again.
The winner(s) will be the person(s) who is closest to 2/3rds of the average of all bids.</p>

<p> Before proceeding to place a bid, please take a moment to think about how to bid in order to maximise your chance of winning.</p>
